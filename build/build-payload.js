'use strict';

// ---------------------------------------------------------------------------
// build-payload.js — bundle the pure engine + DOM layer into ONE self-contained
// IIFE string that can be prepended to Claude's renderer bundles (desktop) or
// wrapped as a userscript (browser).
//
// It uses a tiny CommonJS shim so every source file keeps its OWN scope: the
// exact same files that Node unit-tests are the ones that ship, with no name
// collisions and no source edits. The CSS is inlined (no external fetch), so the
// payload obeys any CSP and has zero network dependencies.
// ---------------------------------------------------------------------------

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const ROOT = path.resolve(__dirname, '..');

// Modules to bundle, keyed by repo-relative id (also how the shim resolves them).
const MODULE_IDS = [
  'engine/ranges.js',
  'engine/numbers.js',
  'engine/detect.js',
  'engine/math.js',
  'engine/arrows.js',
  'engine/relations.js',
  'engine/code.js',
  'engine/index.js',
  'dom/surfaces.js',
  'dom/apply.js',
];

const ENTRY = 'dom/apply.js';
const MARKER = 'claude-rtl-payload';

function readVersion() {
  try {
    const pkg = JSON.parse(fs.readFileSync(path.join(ROOT, 'package.json'), 'utf8'));
    return pkg.version || '0.0.0';
  } catch (e) {
    return '0.0.0';
  }
}

function buildPayload() {
  const version = readVersion();
  const css = fs.readFileSync(path.join(ROOT, 'dom', 'apply.css'), 'utf8');

  let defs = '';
  for (const id of MODULE_IDS) {
    let src = fs.readFileSync(path.join(ROOT, id), 'utf8');
    if (id === ENTRY) {
      // Inline the CSS and stamp the version into the entry module.
      src = src.replace("'__APPLY_CSS__'", JSON.stringify(css));
      src = src.replace(/__PAYLOAD_VERSION__/g, version);
    }
    // Wrap each file as a CommonJS module factory, preserving its own scope.
    defs +=
      '__def(' +
      JSON.stringify(id) +
      ', function (module, exports, require) {\n' +
      src +
      '\n});\n';
  }

  return (
    ';(function () {\n' +
    "'use strict';\n" +
    '/* ' +
    MARKER +
    ' v' +
    version +
    ' — generated by build/build-payload.js; do not edit */\n' +
    'var __mods = {}, __cache = {};\n' +
    'function __def(id, fn) { __mods[id] = fn; }\n' +
    'function __dir(id) { var i = id.lastIndexOf("/"); return i < 0 ? "" : id.slice(0, i); }\n' +
    'function __norm(p) {\n' +
    '  var parts = p.split("/"), out = [];\n' +
    '  for (var i = 0; i < parts.length; i++) {\n' +
    '    if (parts[i] === "" || parts[i] === ".") continue;\n' +
    '    if (parts[i] === "..") out.pop(); else out.push(parts[i]);\n' +
    '  }\n' +
    '  return out.join("/");\n' +
    '}\n' +
    'function __resolve(from, req) {\n' +
    '  if (req.charAt(0) !== ".") return req;\n' +
    '  return __norm(__dir(from) + "/" + req);\n' +
    '}\n' +
    'function __req(id) {\n' +
    '  if (__cache[id]) return __cache[id].exports;\n' +
    '  var m = { exports: {} };\n' +
    '  __cache[id] = m;\n' +
    '  var fn = __mods[id];\n' +
    '  if (!fn) throw new Error("claude-rtl: missing module " + id);\n' +
    '  fn(m, m.exports, function (p) { return __req(__resolve(id, p)); });\n' +
    '  return m.exports;\n' +
    '}\n' +
    defs +
    'try { __req(' +
    JSON.stringify(ENTRY) +
    '); } catch (e) { if (typeof console !== "undefined") console.error("[claude-rtl]", e); }\n' +
    '})();\n'
  );
}

function buildUserscript(payload) {
  const version = readVersion();
  const header =
    '// ==UserScript==\n' +
    '// @name         Claude RTL\n' +
    '// @namespace    https://github.com/eliranpv11/claude-desktop-rtl\n' +
    '// @version      ' +
    version +
    '\n' +
    '// @description  Smart right-to-left (Hebrew/Arabic/Persian) for claude.ai\n' +
    '// @match        https://*.claude.ai/*\n' +
    '// @run-at       document-start\n' +
    '// @grant        none\n' +
    '// ==/UserScript==\n';
  return header + payload;
}

// Verify the bundle is syntactically valid JS (parses without executing).
function assertParses(payload) {
  new vm.Script(payload, { filename: 'claude-rtl-payload.js' });
}

function main() {
  const payload = buildPayload();
  assertParses(payload);
  const dist = path.join(ROOT, 'dist');
  if (!fs.existsSync(dist)) fs.mkdirSync(dist, { recursive: true });
  fs.writeFileSync(path.join(dist, 'payload.js'), payload, 'utf8');
  fs.writeFileSync(path.join(dist, 'claude-rtl.user.js'), buildUserscript(payload), 'utf8');
  const kb = (Buffer.byteLength(payload, 'utf8') / 1024).toFixed(1);
  process.stdout.write('Built dist/payload.js (' + kb + ' KB) and dist/claude-rtl.user.js\n');
}

module.exports = { buildPayload, buildUserscript, assertParses, MARKER };

if (require.main === module) main();
